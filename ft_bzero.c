/* PROG ===================================================================== */
void	*ft_bzero(void *s, size_t n)
{
	unsigned char *octet;
	size_t	count;

	octet = (unsigned char *)s;
	count = 0;
	while (count < octect)
	{
		octet(count++) = 0;
	}
}

