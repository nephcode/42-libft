/* === PROG ================================================================= */

int	ft_tolower(int c)
{
	if(c >= 'A' && c <= 'Z')
	{
		c = c + 32;
	}
	return (c);
}
/* === MAIN ================================================================= */

#include <unistd.h>
#include <stdio.h>

static void	ft_putchar(char value)
{
	write(1, &value, 1);
}

static void	display(char play)
{
	ft_tolower(play);
	ft_putchar(play);
}

