char ft_tolower(char *c)
{
	int count;

	count = 0;
	while(c[count] != '\0')
	{
		if(c[count] >= 'a' && c[count] <= 'z')
		{
			c[count] = c[count] + 32;
		}
		count++;
	}
	return(c);
}
