#include <unistd.h>
/* PROG ===================================================================== */
void	ft_putendl_fd(char *s, int fd)
{
	while(*s)
	{
		write(fd, s, 1);
		s++;
	}
	write(fd, "\n", 1);
}

/* MAIN ===================================================================== */
int	main(int argc, char **argv)
{
	if (argc != 2)
		ft_putendl_fd("Saisi un argument", 1);
	else
		ft_putendl_fd(argv[1], 1);
	return (0);
}
