static size_t ft_strlen(char *chain)
{
	char	 *value;

	value = chain;
	while(*value)
	{
		value++;
	}
	return (value - chain);
}

// -------------------------------------------------------------------------- //

char	*strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	count;
	size_t			length;
	char			*value;

	if (!s || !f)
		return (NULL);
	length = ft_strlen(s);
	value = (char *)malloc(sizeof(char) * (length + 1));

}
