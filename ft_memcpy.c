#include <stddef.h>
/* PROG ===================================================================== */
void	ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t				count;
	unsigned char		*d;
	unsigned const char	*s;
	
	d = (unsigned char*)dest;
	s = (unsigned const char*)src;
	count = 0;
	while (count < n)
	{
		d[count] = s[count];
		count++;
	}
	return (dest);
}

