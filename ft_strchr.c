char	*strchr(const char *s, int c)
{
	int		count;
	char	result;

	count = 0;
	if(!(s[count] == c && c == '\0'))
	{
		while(s[count] != c)
		{
			if (s[count] == c)
				&s = c;
			count++;
		}
		return (s);
	}
}
